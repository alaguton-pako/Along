import * as React from "react";
import Button from "@mui/material/Button";
import Menu from "@mui/material/Menu";
import MenuItem from "@mui/material/MenuItem";
import { useNavigate } from "react-router-dom";

export default function BasicMenu() {
  const navigate = useNavigate();
  const [anchorEl, setAnchorEl] = React.useState<null | HTMLElement>(null);
  const open = Boolean(anchorEl);
  const handleClick = (event: React.MouseEvent<HTMLButtonElement>) => {
    setAnchorEl(event.currentTarget);
  };
  const handleClose = () => {
    setAnchorEl(null);
  };

  return (
    <div>
      <Button
        sx={{
          textTransform: "initial",
          fontSize: "1.4rem",
          color: "black",
        }}
        id="basic-button"
        aria-controls={open ? "basic-menu" : undefined}
        aria-haspopup="true"
        aria-expanded={open ? "true" : undefined}
        onClick={handleClick}
      >
        Categories
      </Button>
      <Menu
        id="basic-menu"
        anchorEl={anchorEl}
        open={open}
        onClose={handleClose}
        MenuListProps={{
          "aria-labelledby": "basic-button",
        }}
      >
        <MenuItem onClick={() => navigate("/resturants")}>Resturants</MenuItem>
        <MenuItem onClick={() => navigate("/hotels")}>Hotels</MenuItem>
        <MenuItem onClick={() => navigate("/malls")}>Malls</MenuItem>
        <MenuItem onClick={() => navigate("/parks")}>Parks</MenuItem>
        <MenuItem onClick={() => navigate("/government-offices")}>
          Government Offices
        </MenuItem>
      </Menu>
    </div>
  );
}
